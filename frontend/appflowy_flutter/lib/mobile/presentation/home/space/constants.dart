/// 空间UI常量类
/// 
/// 定义移动端空间(Space)相关的UI常量，用于保持整个应用中空间组件的一致性。
/// 这些常量主要用于空间列表、空间项等组件的尺寸控制。
/// 
/// 设计思想：
/// - 集中管理空间相关的UI常量，便于维护和修改
/// - 确保所有空间组件使用统一的尺寸标准
/// - 提供清晰的命名规范，增强代码可读性
/// - 支持未来扩展更多空间相关的常量定义
class SpaceUIConstants {
  /// 空间列表项的标准高度
  /// 
  /// 这个值用于控制空间列表中每个空间项的高度，包括：
  /// - 空间名称显示区域
  /// - 空间图标显示区域  
  /// - 内部垂直边距
  /// - 触摸目标区域
  /// 
  /// 52.0px的高度确保了：
  /// 1. 足够的触摸目标大小（符合Material Design指导原则）
  /// 2. 良好的视觉层次和间距
  /// 3. 与其他移动端组件高度的协调性
  /// 4. 在不同屏幕密度下的一致显示效果
  static const itemHeight = 52.0;
}
